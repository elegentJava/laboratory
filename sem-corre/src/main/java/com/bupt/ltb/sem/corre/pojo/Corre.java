package com.bupt.ltb.sem.corre.pojo;

/**
 * 函电实体
 * 
 * @author Hogan
 *
 */
public class Corre {

	private Integer correId;
	private String correEnglish;
	private String correChinese;
	private Integer correCategory;

	public Integer getCorreId() {
		return correId;
	}

	public void setCorreId(Integer correId) {
		this.correId = correId;
	}

	public String getCorreEnglish() {
		return correEnglish;
	}

	public void setCorreEnglish(String correEnglish) {
		this.correEnglish = correEnglish;
	}

	public String getCorreChinese() {
		return correChinese;
	}

	public void setCorreChinese(String correChinese) {
		this.correChinese = correChinese;
	}

	public Integer getCorreCategory() {
		return correCategory;
	}

	public void setCorreCategory(Integer correCategory) {
		this.correCategory = correCategory;
	}

}
